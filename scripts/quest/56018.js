var status = -1;

function start(mode, type, selection) {
	if (mode == -1) {
		qm.dispose();
	} else {
		if (mode == 1)
			status++;
		else
			status--;
		if (status == 0) {
			qm.sendOk("恭喜你获得系统奖励！\r\n\r\n1,000,000金币和200张转蛋卷.");
			qm.gainMeso(1000000);
			qm.gainItem(5220040, 200);
			qm.forceCompleteQuest();
		  qm.dispose();	
		} 
	}
}